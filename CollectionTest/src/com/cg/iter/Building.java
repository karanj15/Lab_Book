package com.cg.iter;

public class Building {
	final int no;
	final String name;
   final String city;
public Building(int no, String name, String city) {
	super();
	this.no = no;
	this.name = name;
	this.city = city;
}
public int getNo() {
	return no;
}
public String getName() {
	return name;
}
public String getCity() {
	return city;
}

}
